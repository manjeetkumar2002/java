// public means this class can be accessiable anywhere
// class contain code and method
// Main is name of file name 

public class Main{
    // main function is entry point for the program 
    // static means we want to run this main function without creating object of the the Main class
    // void is type of return value , void means main function nothing returing 
    // String[] is array of strings  , args is name of the array
    public static void main(String[] args){
        System.out.println("Hello world!");
    }
}
// command to convert Main.java into Main.class :- javac Main.java 
// How to run Main.class file  :- command is (java Main)
// javac -d location name where we want to store .class file
// javac is compiler